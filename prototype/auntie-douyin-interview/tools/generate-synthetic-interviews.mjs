async function main() {
  const submitted = [];
  for (const role of roles) {
    const response = await fetch(`${baseUrl}/api/submit`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ interviewee: role.name, answers: role.answers, synthetic: true, traits: role.traits }),
    });
    const data = await response.json();
    if (!response.ok || !data.ok) throw new Error(data.error || `submit failed for ${role.name}`);
    submitted.push({ role: role.name, submissionId: data.submissionId, insight: data.insight });
  }
  console.log(JSON.stringify({ ok: true, count: submitted.length, submitted }, null, 2));
}

